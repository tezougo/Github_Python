def dijkstra(matriz, vertice_inicial):
    n = len(matriz)
    caminho_mais_curto = [float('inf')] * n
    caminho_mais_curto[vertice_inicial] = 0
    visitados = [False] * n
    caminhos = {}
    u = vertice_inicial

    while True:
        distancia_minima = float('inf')
        visitados[u] = True

        for k in range(n):
            if not visitados[k] and matriz[u][k] and caminho_mais_curto[u] + matriz[u][k] < caminho_mais_curto[k]:
                caminho_mais_curto[k] = caminho_mais_curto[u] + matriz[u][k]
                caminhos[(u+1, k+1)] = matriz[u][k]

        u_next = -1
        for i in range(n):
            if not visitados[i] and caminho_mais_curto[i] < distancia_minima:
                u_next = i
                distancia_minima = caminho_mais_curto[i]

        if u_next == -1:
            break
        
        u = u_next

    return caminhos





